		<h3>Eneida</h3>
		<p>
			 La Eneida está dividida en doce cantos o libros; sucintamente, el argumento de cada uno de ellos es el siguiente:

<p>CANTO I: Eneas, próximo a llegar a Italia con su flota, es rechazado por una tempestad ordenada por Juno, su enemiga implacable. Es arrojado a las costas de África, perdiendo tres navios y dis­persándose el resto de ellos. Con su fiel Acates, penetra en el interior y llega a Cartago. La reina Dido, que ha debido huir de Tiro, su ciudad natal, y que está por fundar una nueva ciudad, lo recibe muy hospitalariamente. Le ofrece un banquete y le pide que narre sus aventuras.</p>

<p>CANTO II: Dedicado a la evocación de la última noche de Troya, con la caída y toma de la ciudad. Eneas debió huir, sin su esposa Creuse, que se ha extraviado, pero con su hijo, su viejo padre Anquises y, sobre todo, sus Penates.</p>

<p>CANTO III: Prosigue la narración del Canto II. Se habla aquí de los “extravíos” de Eneas en Tracia, en Délos, en la costa del Epiro; y luego, hacia el sur de Italia, donde consulta a la sibila de Cumas. En Sicilia, Eneas y sus compañeros son recibidos por Alcestes; transcurren allí un invierno, durante el cual muere Anquises.</p>

<p>CANTO IV: Mientras tanto, Dido, bajo la influencia de Venus, se ha enamorado de Eneas. Este amor apasionado, pero imposible, es el lema del canto. Eneas recibe de Júpiter orden de huir. Dido, desesperada, se suicida.</p>

<p>CANTO V: Eneas vuelve con Alcestes y honra con juegos fúnebres el aniversario de la muerte de su padre. Deja en Sicilia a sus compañeros más cansados para que funden una ciudad, y parte para Italia.</p>

<p>CANTO VI: En este canto, verdadero corazón del poema, Eneas consulta en Cumas a la sibila. La sacerdotisa de Apolo lo provee del medio para descender a los infiernos. Eneas atraviesa la Estigia. ve el Tártaro y alcanza los Campos Elíseos. Allí encuentra a su padre Anquises, quien le muestra las almas de los futuros romanos ilustres, entre otras la de Augusto.</p>

<p>CANTO VII: Eneas llega por fin al Lacio. Pretende desposar a Lavinia, hija del rey Latino, señor del Lacio, pero Turno, príncipe de los rútulos, influido por Juno, reclama también la mano de Lavinia. Ello desata la guerra.</p>

<p>CANTO VIII: Eneas remonta el Tíber justo hasta el emplazamiento de la futura Roma. Allí domina el rey arcadio Evandro. Eneas es bien recibido por este rey de raza griega y por su hijo Palas. Durante este tiempo, Venus logra de Vulcano que forje un escudo para Eneas, en el que se representan grandes escenas de la historia romana, entre otras la batalla de Accio.</p>

<p>CANTO IX: Eneas va en busca del apoyo de las ciudades etruscas. Turno ataca el campo troyano, incendia los barcos y logra adueñarse de casi todo el campo. Dos jóvenes, Niso y Eurialo, intentan en vano franquear las líneas rútulas para alcanzar a Eneas e informarle de la situación; caen heridos de muerte.</p>

<p>CANTO X: En una asamblea de los dioses, Juno y Venus se enfrentan; pero Júpiter deja que el destino decida. Eneas, a la cabeza de un ejército etrusco, rebelado contra la tiranía del rey Mezencio, gana una batalla sobre los rútulos, pero Palas es muerto por Turno.</p>

<p>CANTO XI: Se entierran los muertos de ambos bandos. Evandro llora a su hijo Palas. Una nueva batalla se desarrolla. Es otra victoria para los troyanos y sus aliados, a pesar del valor guerrero de la reina de los volscos, Camila, a quien una flecha hiere mortalmente. Los hombres de Latino retroceden en desorden.</p>

<p>ANTO XII: La suerte de la guerra se decidirá en un combate personal entre Turno y Eneas. No obstante la ayuda de su hermana, la ninfa Jaturna, Turno es finalmente vencido y muerto. Eneas podrá casarse con Lavinia y reinar sobre un pueblo nuevo, surgido de la fusión de troyanos y latinos.</p>


